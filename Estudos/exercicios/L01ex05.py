#Ler três valores e imprimir a soma dos quadrados dos três valores
print('Somando o valor da área de três quadrados!')
lado1 = float(input("Digite o primeiro valor: "))
lado2 = float(input("Digite o segundo valor: "))
lado3 = float(input("Digite o terceiro valor: "))

quadrado1 = lado1 * 4
quadrado2 = lado1 * 4
quadrado3 = lado1 * 4
soma = quadrado1 + quadrado2 + quadrado3

print('A soma da áera dos três quadrados é: {}' .format(soma))
